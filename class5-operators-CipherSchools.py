# Remember
# true or a = true          -> a or b = a (if a is truthy)
# false or a = a            -> a or b = b (if a is falsy)
# true and a = a            -> a and b = b (if a is truthy)
# false and a = false       -> a and b = a (if a is falsy)

# Truthy = True, positive-numbers, non-empty strings, non-empty array
# Falsy = False, 0, "", []

print("12134" or False)
print(0 and 1)
print(1 and 5)
print("" and True)
print(0 and "")
print([1,2,3] or True)

